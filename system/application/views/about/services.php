<h3>Session Setup</h3>
<p>
So you've set up your event and it's been approved - what's next? Well, we have self-service methods you can
use to load in the sessions for your event - an event import and adding them through the interface - but sometimes
there's just not time for that. Whether it's a last minute event addition or there aren't quite enough hands to go around, 
we can help. 
</p>
<p>
You provide us with the session information and we'll be sure it gets into your event. A well-structured
document is appreciated but not required. Contact us at <a href="services@joind.in">services@joind.in</a> to get started.
</p>
