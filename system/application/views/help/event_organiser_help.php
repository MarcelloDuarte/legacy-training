<h2>Joind.In For Event Organisers</h2>

<p>Joind.in is free to use for event organisers, and we welcome all kinds of events who are looking for a good way to get <b>immediate, direct feedback</b> from their attendees.  Read on to find out more about using joind.in for your event.</p>

<h3>Submitting an Event</h3>

<p>To get your event listed on joind.in, you'll need to <a href="/event/submit">submit it to us</a> for approval.  Please make sure that you read the requirements on the submission page, and fill in all required fields.  We have a team of volunteer approvers so your event should be approved within a day or two.  Once your event is confirmed, it will be publicly available and you can start adding sessions to it.</p>

<h3>Adding Additional Event Admins</h3>

<p>If you're organising an event with a team, you may wish to add more administrators to the event.  To do this, you can simply add their name in the "Admins" section on the right hand side of the event page.</p>

<h3>Adding Sessions to an Event</h3>

<p>You can add sessions from the event page, using the "Add Session" link on the right hand bar.  If you have a large number of sessions to add, you might like to check out the "Import Event Info" feature also linked from the sidebar; this allows a spreadsheet of talk titles, speakers and descriptions to be uploaded all in one go - making the process much quicker and easier for you</p>

<h3>Linking Speakers and Talks</h3>

<p>Speakers can associate the talks they give with their user accounts on joind.in.  To do this, those users need to register for a joind.in account, visit the page of the talk, and <b>claim</b> it.  Claims aren't finalised until the event admin (that's you!) authorises the claim; you can see that you have claims waiting for your attention on the sidebar.  The number after "Pending Claims" tells you how many there are.</p>

<p>Joind.in can handle multiple speakers per talk; each speaker needs to claim the talk and indicate which of the multiple speakers they are.  You can then authorise the claim in the normal way</p>

<h3>Comments</h3>

<p>Joind.in is all about getting feedback to the people who need it as quickly as possible.  Users can leave feedback from the day the talk happens, until 12 weeks after the event.  There are a number of different types of comment:
<ul><li><b>Attendee comments:</b> These are standard comments, including some text and a rating.</li>
<li><b>Anonymous comments:</b> Users don't need a user account on joind.in to give feedback on the talks they've seen, however anonymous users' ratings aren't included when calculating the talk averages</b></li>
<li><b>Private comments:</b> It is possible for users to mark their comments as private; these will be visible only to the speakers and event admins</li>
<li><b>Speaker comments:</b> Speakers commenting on their own talk cannot rate it, and these are highlighted when the comments are shown on the site</li>
<li><b>Reply comments: </b>Where a user has already commented on a talk, they can comment again but cannot rate their later comments.</li></ul>

<p>Any user can report a comment as spam; if this happens for any comment on your event, you will immediately get an email containing information about the report and a link to the exact comment that was reported.  You have the rights to delete any comment at any time.</p>

<h3>Anything Else?</h3>

<p>Some final hints and tips for you:
<ul><li>If you're having a call for papers, register your event well in advance and include information about call for papers dates; we feature events with current CfPs on the site</li>
<li>The events with a good amount of useful feedback tend to be those where organisers and speakers publicise the site.  Get speakers to claim their talks and include links on their slides for best results</li>
<li>We have mobile apps for iphone and android, plus an API which you can use to integrate with any of the tools you're already using.</li></ul></p>

<p>For anything else, please do <a href="/about/contact">get in touch</a>, we love to hear about how you're using the site, and of course any questions, comments or suggestions you have.</p>
