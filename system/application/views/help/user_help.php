<h2>User's Guide to Joind.in</h2>

<h3>Registering an Account</h3>

<p>It is free to register on joind.in, simply click on the "Register" button in the top right, and add your details.</p>

<h3>Linking to Twitter</h3>

<p>You can add your twitter account in your user profile, simply click on "Account" at the top right, then use the "Manage account" tab.  Enter your twitter name and save the record.  Sharing your twitter nick is a great way to find people on twitter at the same event as you</p>

<h3>Adding an Avatar</h3>

<p>All the avatar images on Joind.in are from <a href="https://gravatar.com">Gravatar</a> - simply register an avatar there (with the same email address you use for joind.in) and your avatar will appear on your account page and with all your comments on joind.in</p>

<h3>I Forgot My Password/Username/Email Address</h3>

<p>Don't panic!  You can request a new password from any of the login forms, and you need to supply either your email or username to do this (if you're not certain of one, simply supply the other).  If you get stuck or you've changed your email address or something, then let us know and we'll make sure you can get access to your account</p>
